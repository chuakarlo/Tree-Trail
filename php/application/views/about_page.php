                                <p align="center" style = "font-size:20px"><font color ="#333">The National Greening Program (NGP) is a massive forest rehabilitation program of the government established by virtue of Executive Order No. 26 issued on Feb. 24, 2011 by President Benigno S. Aquino III. It seeks to grow 1.5 billion trees in 1.5 million hectares nationwide within a period of six years, from 2011 to 2016. Aside from being a reforestation initiative, the NGP is also seen as a climate change mitigation strategy as it seeks to enhance the country’s forest stock to absorb carbon dioxide, which is largely blamed for global warming. It is also designed to reduce poverty, providing alternative livelihood activities for marginalized upland and lowland households relating to seedling production and care and maintenance of newly-planted trees. As a convergence initiative among the Departments of Agriculture, Agrarian Reform and DENR, half of the targeted trees to be planted under the program would constitute forest tree species intended for timber production and protection as well. The other 50% would comprised of agroforestry species. Areas eligible for rehabilitation under the program include all lands of the public domain. Spefically, these include forestlands, mangrove and protected areas, ancestral domains, civil and military reservation, urban greening areas, inactive and abandoned mine sites and other suitable lands.</font>
